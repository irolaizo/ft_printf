void	ft_putnbr_fd(int n)
{
	char	*s;
	int		len;

	s = "0123456789";
	len = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		ft_putchar ('-', 1);
		n = n * -1;
		len ++;
	}
	if (n > 9)
		len += ft_putnbr (n / 10, 1);
	write(1, &s[n % 10], 1);
	len ++;
	return (len);
}